/* 1. \n - New line
 * 2. \t - Tab space
 * 3. \' - Single quote
 * 4. \\ - Backslash
 */

public class _03_3_Space_sequence_character
{
    public static void main(String[] args)
    {
        System.out.println("This is a space sequence character");
        System.out.println("This is a \"space sequence character");
        System.out.println("This is a \"space sequence\" character");
//        System.out.println("This is a \'space sequence\' character");
        System.out.println("This is a space sequence character\\");
        System.out.println("\tThis is a space sequence character");


    }
}

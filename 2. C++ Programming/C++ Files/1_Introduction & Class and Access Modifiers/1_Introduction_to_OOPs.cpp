// Object Oriented Programming - Object-oriented programming (OOP) is a computer programming model that organizes software design around data, or objects, rather than functions and logic. An object can be defined as a data field that has unique attributes and behavior.

// 1.C++ language was designed with the main intention of adding object-oriented features to C language.

// 2.As the size of program increases readability, maintainability and bug-free nature of a program decreases.

// 3.This was the major problem with language like C which relied upon function or procedures (hence the name procedural programming languge)

// 4.As a result, the possibility of not addressing the problem in an effective manner was high.

// 5.Also, as data was almost neglected, data security was easily compromised.

// 6.Using classes solves this problem by modelling progarm as a real world scenario.

// ......Procedure Oriented programming......
// 1.Consists of writing a set of instruction for the computer to follow.
// 3.Main focus is on function and not on flow of data.
// 4.Function can either use local or global data.
// 5.Data moves openly form function to function.

// ..... Object oriented programming.....
// 1.Works on the concept of classes and objects.
// 2.A class is a template to create objects.
// 3.Treats data as a critical element.
// 4.Decompose the problem in objects and builds data and function around the objects.

// ....Benefits of object oriented programming....
// Better code reusability and inheritance.
// Principle of data hiding helps build secure systems.
// Multiple objects can co-exits without and interference.
// Software complexity can be easily managed.

// .....Basic concepts in object oriented programming.....
// Classes - Basic template for creating objects.
// Objects - Basic run time entities.
// Data Abstraction - Hiding the implemention.
// Encapsulation - Wrapping data and function into single unit.
// Inheritance - Properties of one class can be inherited in others.
// Polymorphism - ability to take more than one forms.
// Dynamic Binding - code which will execute is not known until the program runs.
// Message Passing - Object.message(information) call format.

// 1. Class -> The building block of C++ that leads to Object-Oriented programming is a Class. It is a user-defined data type, which holds its own data members and member functions, which can be accessed and used by creating an instance of that class. A class is like a blueprint for an object. For Example: Consider the Class of Cars. There may be many cars with different names and brands but all of them will share some common properties like all of them will have 4 wheels, Speed Limit, Mileage range, etc. So here, the Car is the class, and wheels, speed limits, and mileage are their properties.

// 2. Object -> An Object is an identifiable entity with some characteristics and behavior. An Object is an instance of a Class. When a class is defined, no memory is allocated but when it is instantiated (i.e. an object is created) memory is allocated.

// 3. Inheritance: The capability of a class to derive properties and characteristics from another class is called Inheritance. 

// 4. Polymorphism: The word polymorphism means having many forms. In simple words, we can define polymorphism as the ability of a message to be displayed in more than one form. A person at the same time can have different characteristics. A man at the same time is a father, a husband, and an employee. So the same person possesses different behavior in different situations. This is called polymorphism.

// 5. Encapsulation: In normal terms, Encapsulation is defined as wrapping up data and information under a single unit. In Object-Oriented Programming, Encapsulation is defined as binding together the data and the functions that manipulate them.

// 6. Abstraction: Data abstraction is one of the most essential and important features of object-oriented programming in C++. Abstraction means displaying only essential information and hiding the details. Data abstraction refers to providing only essential information about the data to the outside world, hiding the background details or implementation.


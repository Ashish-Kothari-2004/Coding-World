// Q. WAP to demonstrate the use of bitwise right sift (>>) operator.
// Q. WAP to demonstrate the use of bitwise left sift (<<) operator.
#include <stdio.h>
int main()
{
    int a;
    printf("Enter any two number\n");
    scanf("%d", &a);
    printf("\n%d\n", a >> 4);// 368 = 101110000 = 368>>4 = 10111 = 23


    return 0;
}